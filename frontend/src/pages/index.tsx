import Head from 'next/head';
import { useAuth } from '../../contexts/AuthContext';
import Dashboard from '@/components/dashboard/Dashboard';
import { useRouter } from 'next/router';
import { useEffect } from 'react';
import PsychiatristDashboard from '@/components/psychDashboard/PsychiatristDashboard';
import FilterUser from '@/components/filter/FilterUser';

const App = () => {
  const { user } = useAuth();
  const router = useRouter();


  // This effect runs when the component mounts or when 'user' or 'router' change.
  useEffect(() => {
    // Check if there is a logged-in user. If 'user' is not null, a user is logged in.
    if (user) {
      // Check if userType exists in the user object
      if (user.userType) {
        // Construct the URL to the user's dashboard based on their unique user ID (UID).
        // This URL will navigate the user to their personalized dashboard.
        if (user.userType == "patient") {
          router.push(`/patient/${user.uid}/dashboard`);
        } else if (user.userType == "psychiatrist") {
          router.push(`/psychiatrist/${user.uid}/psych_dashboard`);
        } else if (user.userType == "admin") {
          router.push(`/admin/${user.uid}/database`);
        }
      } else {
        console.log('User type not found');
      }
    } else {
      console.log('No user logged in');
      router.push(`/discover`);
    }
  }, [user, router]);

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/logo.svg" type="image/svg+xml" />
      </Head>
      {user && user.userType == "patient" && <Dashboard />}
      {user && user.userType == "psychiatrist" && <PsychiatristDashboard />}
      {user && user.userType == "admin" && <FilterUser />}
    </>
  );
};

export default App;
